void main(List<String> args) {
  // Built-in-DataTypes

// # number
// ## for decimal number
  int id_no = 30;
  print(id_no);

// ## for decimal point number
  double wight = 10.10;
  print(wight);

// # string
  String name = "Rejaul";
  print(name);

// # boolean data
  bool isLogin = true;
  print(isLogin);

// Dynamic data
  var quantity = 100;
  print(quantity);

  var cost = 100.50;
  print(cost);

  var gender = "Male";
  print(gender);

  var is_full = true;
  print(is_full);

  // # Check Data Type
  print(wight.runtimeType);
  print(id_no.runtimeType);
  print(name.runtimeType);
  print(isLogin.runtimeType);
  print(quantity.runtimeType);
  print(cost.runtimeType);
  print(gender.runtimeType);
  print(is_full.runtimeType);
}
